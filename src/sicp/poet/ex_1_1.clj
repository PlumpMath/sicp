(def 문제
  "아래에 여러 식이 있다. 실행기가 찍어 내는 값은 무엇인가? 아래에 적힌 식의 값을 차례대로 구한다고 하자.")

10
;; 10

(+ 5 3 4)
;; 12

(- 9 1)
;; 8

(/ 6 2)
;; 3

(+ (* 2 4) (- 4 6))
;; 6

(def a 3)
;; #'user/a

(def b (+ a 1))
;; #'user/b

(+ a b (* a b))
;; 19

(= a b)
;; false

(if (and (> b a) (< b (* a b)))
  a
  b)

(cond (= a 4) 6
      (= b 4) (+ 6 7 a)
      :else 25)
;; 16

(+ 2 (if (> b a) b a))
;; 6

(* (cond (> a b) a
         (< a b) b
         :else -1)
   (+ a 1))
;; 16